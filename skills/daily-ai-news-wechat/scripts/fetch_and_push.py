def load_accounts():
    """Load list of public accounts to follow"""
    accounts_path = os.path.join(skill_dir, "references", "accounts.md")
    accounts = []
    with open(accounts_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line.startswith("- `"):
                # Extract account name from format: - `Name` : Description
                name_part = line.split("`")[1]
                accounts.append(name_part)
    return accounts

