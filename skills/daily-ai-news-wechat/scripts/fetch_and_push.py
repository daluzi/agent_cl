def format_digest(summarized_articles):
    """Format the daily digest for WeChat sending"""
    today = datetime.now().strftime("%Y年%m月%d日")
    digest = f"📰 每日AI早报 - {today}\n\n"
    
    if not summarized_articles:
        digest += "今天没有获取到新的相关文章。"
        return digest
    
    for i, article in enumerate(summarized_articles, 1):
        digest += f"{i}. **{article['title']}**\n"
        digest += f"🔗 {article['url']}\n"
        digest += f"📝 {article['summary']}\n\n"
    
    digest = digest.strip()
    return digest

